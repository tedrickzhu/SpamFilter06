package bistu.zzy.spamfilter.object;

public class Mail {

	private String MailName = null;

	private String MailPath = null;

	private String MailHead = null;

	private String MailContent = null;

	public String getMailName() {
		return MailName;
	}

	public void setMailName(String mailName) {
		MailName = mailName;
	}

	public String getMailPath() {
		return MailPath;
	}

	public void setMailPath(String mailPath) {
		MailPath = mailPath;
	}

	public String getMailHead() {
		return MailHead;
	}

	public void setMailHead(String mailHead) {
		MailHead = mailHead;
	}

	public String getMailContent() {
		return MailContent;
	}

	public void setMailContent(String mailContent) {
		MailContent = mailContent;
	}

}
